# check for even or odd

userInp = int(input('Enter number to check even or odd: '))

if (userInp % 2) == 0:
    print('Number is even.')
else:
    print('Number is odd.')


# Example of ELIF:
x = userInp

if x == 1:
    print('One')
elif x == 2:
    print('Two')
else:
    print('None')

    
